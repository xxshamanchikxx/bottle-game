// Функция для получения случайного вопроса/задания
export function getRandomTruth(gender) {
  const questions = truthQuestions[gender] || truthQuestions.male;
  return questions[Math.floor(Math.random() * questions.length)];
}

export function getRandomDare(gender) {
  const actions = dareActions[gender] || dareActions.male;
  return actions[Math.floor(Math.random() * actions.length)];
}
